package com.aula;

public class CadastroAtendimento {
	
	private String nomeProp, nomePet, racaPet ,tipoService;
	
	public String getNomeProp() {
		return nomeProp;
	}
	
	public void setNomeProp(String nomeProp) {
		this.nomeProp = nomeProp;
	}
	
	public String getNomePet() {
		return nomePet;
	}
	
	public void setNomePet(String nomePet) {
		this.nomePet = nomePet;
	}
	
	public String getRacaPet() {
		return racaPet;
	}
	
	public void setRacaPet(String racaPet) {
		this.racaPet = racaPet;
	}
	
	public String getTipoService() {
		return tipoService;
	}
	
	public void setTipoService(String tipoService) {
		this.tipoService = tipoService;
	}

}
